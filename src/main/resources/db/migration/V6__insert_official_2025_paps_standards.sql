       CASE derived_template.max_col WHEN 1 THEN source.t1 WHEN 2 THEN source.t2 WHEN 3 THEN source.t3 WHEN 4 THEN source.t4 ELSE NULL END,
       derived_template.minimum_inclusive,
       derived_template.maximum_inclusive,
       CURRENT_TIMESTAMP(6), CURRENT_TIMESTAMP(6)
FROM tmp_paps_standard_source source
JOIN paps_standard_version version ON version.code = 'PAPS_OFFICIAL_2025_V1'
JOIN fitness_test_item item ON item.code = source.item_code
JOIN (
    SELECT 'HIGHER' direction, 5 grade, NULL min_col, 1 max_col, TRUE minimum_inclusive, FALSE maximum_inclusive UNION ALL
    SELECT 'HIGHER', 4, 1, 2, TRUE, FALSE UNION ALL
    SELECT 'HIGHER', 3, 2, 3, TRUE, FALSE UNION ALL
    SELECT 'HIGHER', 2, 3, 4, TRUE, FALSE UNION ALL
    SELECT 'HIGHER', 1, 4, NULL, TRUE, TRUE UNION ALL
    SELECT 'LOWER', 1, NULL, 1, TRUE, FALSE UNION ALL
    SELECT 'LOWER', 2, 1, 2, TRUE, FALSE UNION ALL
    SELECT 'LOWER', 3, 2, 3, TRUE, FALSE UNION ALL
    SELECT 'LOWER', 4, 3, 4, TRUE, FALSE UNION ALL
    SELECT 'LOWER', 5, 4, NULL, TRUE, TRUE
) derived_template ON derived_template.direction = source.direction;

