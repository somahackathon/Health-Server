CREATE TABLE paps_bmi_standard (
    id BIGINT NOT NULL AUTO_INCREMENT,
    version_id BIGINT NOT NULL,
    school_level VARCHAR(30) NOT NULL,
    school_grade INT NOT NULL,
    gender VARCHAR(20) NOT NULL,
    category VARCHAR(30) NOT NULL,
    minimum_value DECIMAL(10, 2) NULL,
    maximum_value DECIMAL(10, 2) NULL,
    minimum_inclusive BOOLEAN NOT NULL,
    maximum_inclusive BOOLEAN NOT NULL,
    created_at DATETIME(6) NOT NULL,
    updated_at DATETIME(6) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_paps_bmi_standard_version FOREIGN KEY (version_id) REFERENCES paps_standard_version (id),
    CONSTRAINT uk_paps_bmi_standard_category UNIQUE (version_id, school_level, school_grade, gender, category),
    CONSTRAINT ck_paps_bmi_standard_school_grade CHECK (school_grade BETWEEN 1 AND 6),
    CONSTRAINT ck_paps_bmi_standard_value_range CHECK (minimum_value IS NULL OR maximum_value IS NULL OR minimum_value <= maximum_value)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX idx_paps_bmi_standard_lookup
